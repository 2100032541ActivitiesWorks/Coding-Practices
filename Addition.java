import java.util.*;

public class Main{
	public static void main(String[] args) {
	    Scanner sc = new Scanner(System.in);
	    int a = sc.nextInt();
	    System.out.println("Enter your First number: "+a);
	    int b = sc.nextInt();
	    System.out.println("Enter your Second number: "+b);
	    int c = a + b;
	    System.out.println("The Total solution is: "+c);
	    sc.close();
	}
}
